//Manual casting ialah cara untuk buang nombor perpuluhan dibelakang
//sebagai contoh jika ada 100.12 output akan jadi 100 sahaja
public class TypeCasting {
    public static void main(String[] args){
        double nombor1 = 10.9;
        nombor1 = (int) nombor1;
        System.out.println(nombor1);
    }
    
}
    

